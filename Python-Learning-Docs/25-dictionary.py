# List -> Array, accessing by index

# Defining an array
data_array = ['john', 'doe', 'smith']

# Accessing elements by index
print(data_array[0])  # Prints the first element ('john')

# Dictionary -> associative array
# Identifier -> Key

# Defining a dictionary
data_dict = {
    'name': 'John',
    'nickname': 'Doe',
    'age': 30,
    'job': 'Developer',
    'skills': ['Python', 'JavaScript', 'SQL']
}

# Accessing values by keys
print(data_dict['nickname'])  # Prints the value associated with 'nickname'
print(data_dict['age'])  # Prints the value associated with 'age'
print(data_dict['skills'])  # Prints the list of skills
