# Lists (using square brackets)
list_data = [10, 2, 4, 3, 2] 
print(list_data)

# Tuples (immutable sequences)
tuple_data = (7, 8, 9, 10)
print(tuple_data)
print(tuple_data[1])  # Accessing element by index

# Sets (unordered collections of unique elements)
set_data = {10, 4, 3, 2, 4, 7, 6, 5}
print(set_data)

# Note: Sets do not support indexing
# print(set_data[0])  # This will cause an error

