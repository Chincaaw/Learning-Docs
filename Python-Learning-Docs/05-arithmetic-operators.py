# arithmetic operators

# Addition (+)
result_addition = 5 + 3  # 5 added to 3
print("Addition:", result_addition)

# Subtraction (-)
result_subtraction = 7 - 2  # 7 subtracted by 2
print("Subtraction:", result_subtraction)

# Multiplication (*)
result_multiplication = 4 * 6  # 4 multiplied by 6
print("Multiplication:", result_multiplication)

# Division (/)
result_division = 8 / 2  # 8 divided by 2
print("Division:", result_division)

# Floor Division (//)
result_floor_division = 9 // 2  # Integer division of 9 divided by 2
print("Floor Division:", result_floor_division)

# Modulus (%)
result_modulus = 11 % 3  # Remainder of dividing 11 by 3
print("Modulus:", result_modulus)

# Exponentiation (**)
result_exponentiation = 2 ** 3  # 2 raised to the power of 3
print("Exponentiation:", result_exponentiation)
