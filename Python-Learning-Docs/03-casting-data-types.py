# casting datatype

# changing one type to another type

# str() to changing to string
# int() to changing to integer
# float() to changing to float
# bool() to changing to boolean

## integer
data_int = 10
print("Data : ", data_int)
print("Type : ", type(data_int))

data_float = float(data_int)
data_str = str(data_int)
data_bool = bool(data_int)

print("Type : ", type(data_float))
print("Type : ", type(data_str))
print("Type : ", type(data_bool))

## float
data_float = 1.5
print("Data : ", data_float)
print("Type : ", type(data_float))

data_int = int(data_float)
data_str = str(data_float)
data_bool = bool(data_float)

print("Type : ", type(data_int))
print("Type : ", type(data_str))
print("Type : ", type(data_bool))