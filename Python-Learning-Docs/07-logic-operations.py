# logic or boolean operators

# not, or, and, xor

x = True
y = False
a = not x # will be true if false and false if true
b = x or y # will be true if either of them is true
c = x and y # will be true if both are true
d = x ^ y # will be true if one is true and the rest are false

print('x not y: ', a)
print('x or y: ', b)
print('x and y: ', c)
print('x xor y: ', d)